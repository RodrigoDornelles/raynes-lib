static struct ChrPPU emulatePPU;

void ChrLoadFile(const char *fileName)
{
    int file = open(fileName, O_RDONLY);
    read(file, emulatePPU.chrAdddress.raw, sizeof(emulatePPU.chrAdddress.raw));
    for(int i; i < sizeof(emulatePPU.chrAdddress.raw); i++) {
        printf("%02x", emulatePPU.chrAdddress.raw[i]);
    }
    close(file);
}

void ChrLoadPalette(const char *paletteBuffer)
{
    Image generatedImg;
    unsigned int spriteId;
    unsigned char paletteId;
    unsigned char pixelColor;
    unsigned char pixelX;
    unsigned char pixelY;
    Color trueColor;

    /** pallete loop **/
    for (;paletteId < 4; spriteId = 0, paletteId++) {
        /** sprite loop */
        for(spriteId = 0; spriteId < 512; spriteId++) {
            generatedImg = GenImageColor(8, 8, Invisible);
                for (pixelX = 0; pixelX < 8; pixelX++){
                    for (pixelY = 0; pixelY < 8; pixelY++){
                        pixelColor = (emulatePPU.chrAdddress.sprite[spriteId].color1[pixelY] >> (7 - pixelX)) & 1;
                        pixelColor|= (emulatePPU.chrAdddress.sprite[spriteId].color2[pixelY] >> (8 - pixelX)) & 2; 
                        trueColor = lockupColors[paletteBuffer[(paletteId*4)+(pixelColor%4)]];
                        ImageDrawPixel(&generatedImg, pixelX, pixelY, trueColor); 
                    }
                }
            emulatePPU.spriteTexture[paletteId][spriteId] = LoadTextureFromImage(generatedImg);
        }
    }
}

void ChrDrawSprite(int spriteId, int posX, int posY, int paletteId)
{
    DrawTexture(emulatePPU.spriteTexture[paletteId][spriteId], posX, posY, WHITE);
}
